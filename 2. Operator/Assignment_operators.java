import java.util.*;

public class Assignment_operators {
  public static void main(String args[]) {
    // Calculate the Assignment operators:-
    int A = 10;
    int B = 5;

    // A=A+B
    System.out.println(A += B);
    // A=A-B
    System.out.println(A -= B);
    // A=A*B
    System.out.println(A *= B);
    // A=A/B
    System.out.println(A /= B);
    // A=A%B
    System.out.println(A %= B);
  }
}
