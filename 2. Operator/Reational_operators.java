import java.util.*;

public class Reational_operators {
  public static void main(String args[]) {
    // Calculate the relational operators:-
    int A = 10;
    int B = 5;

    System.out.println(A == B);
    System.out.println(A != B);
    System.out.println(A > B);
    System.out.println(A < B);
    System.out.println(A >= B);
    System.out.println(A <= B);
  }
}
